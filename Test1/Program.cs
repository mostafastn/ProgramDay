    public static string GetMafiaResult(int[,] matrix)
    {
        int n = matrix.GetLength(0);
        StringBuilder result = new StringBuilder();

        for (int i = 0; i < n; i++)
        {
            int accusations = 0;
            for (int j = 0; j < n; j++)
            {
                if (matrix[j, i] == -1)
                {
                    accusations++;
                }
            }
            // اگر تعداد اتهامات کمتر از 3 باشد، بازیکن مافیاست
            result.Append(accusations < 3 ? "1" : "0");
        }

        return result.ToString();
    }

